package Topshiriq_1;

public class Sugar {
    private  double sugarAmount;
    private  double sugarPrice;

    public Sugar() {

    }

    public Sugar(double sugarAmount, double sugarPrice) {
        this.sugarAmount = sugarAmount;
        this.sugarPrice = sugarPrice;
    }

    public double getSugarAmount() {
        return sugarAmount;
    }

    public double getSugarPrice() {
        return sugarPrice;
    }
}
